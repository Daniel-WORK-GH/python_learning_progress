#using the basic programming math operators - + / * %, and some unique to python ** //
#we can write simple-hard equations

#standard operators
addition = 2 + 3 #result : 5
subtraction = 5 - 2 #result : 3
division = 5 / 2 #result : 2.5
multiplication = 5 * 3 #result : 15
reminder = 7 % 3 #result : 1

#unique operators
whole_division = 5 // 2 #will return a whole number, result 2
power = 2 ** 3 #2 to the power of 3 (2^3), result 8

#resulting values printed
print("2 + 3 =", addition)
print("5 - 2 =", subtraction)
print("5 / 2 =", division)
print("5 * 3 =", multiplication)
print("7 % 3 =", reminder)

print("5 // 2 =", whole_division)
print("2 ** 3 =",power)


